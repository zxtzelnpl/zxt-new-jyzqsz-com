<script>
    $(function () {
      var lis = $('.content-nav-li');
      var boxes = $('.content-body-box');
      var ACTIVE = 'active';
      lis.on('click',function(){
        var $this =$(this);

        if($this.hasClass(ACTIVE)){
          return false
        }
        else{
          var i = $this.index();
          $this.addClass(ACTIVE).siblings().removeClass(ACTIVE);
          boxes.eq(i).addClass(ACTIVE).siblings().removeClass(ACTIVE);
        }
      });


      /*关于黑名单*/
      (function () {

        function formatDate(index,data) {
          return '' +
            '<tr>' +
            '  <td class="center">'+index+'</td>' +
            '  <td class="p20">'+data.text+'</td>' +
            '  <td class="center">'+data.type+'</td>' +
            '  <td class="center">'+data.time+'</td>' +
            '</tr>'
        }


        var blackListOneHtml = blackListOne.reduce(function(total,currentValue,currentIndex){
          total+=formatDate(currentIndex+1,currentValue);
          return total;
        },'');

        var blackListTwoHtml = blackListTwo.reduce(function(total,currentValue,currentIndex){
          total+=formatDate(currentIndex+blackListOne.length+1,currentValue);
          return total;
        },'');

        $('#blackListOne').after(blackListOneHtml);
        $('#blackListTwo').after(blackListTwoHtml);

      }())
    })
</script>
